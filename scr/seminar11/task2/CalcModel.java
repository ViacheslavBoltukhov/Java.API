package scr.seminar11.task2;

public class CalcModel {

    double summary(double arg1, double arg2){
        return arg1 + arg2;
    }
    double difference(double arg1, double arg2){
        return arg1 - arg2;
    }
    double multiplication(double arg1, double arg2){
        return arg1 * arg2;
    }
    double division(double arg1, double arg2){
        return arg1 / arg2;
    }

}
